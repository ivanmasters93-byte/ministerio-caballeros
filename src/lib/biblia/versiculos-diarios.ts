/**
 * Devuelve el versículo del día de forma determinista basado en la fecha.
 * Todos los usuarios ven el mismo versículo el mismo día.
 */
export function getVersiculoDelDia(date: Date = new Date()): VersiculoDiario {
  const start = new Date(date.getFullYear(), 0, 0)
  const diff = date.getTime() - start.getTime()
  const dayOfYear = Math.floor(diff / 86400000)
  const index = dayOfYear % VERSICULOS_DIARIOS.length
  return VERSICULOS_DIARIOS[index]
}
